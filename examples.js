// write event directly by
[ 
  [ 0, 60, 100, 1 ],
  [ 1, 62, 100, 2 ],
  [ 1, 65, 100, 2 ],
]

////////////////////////////////////////////////////////////////////////////
// loop the same 3 notes (arp)
// this is loop directly after the 0.25 event
[ mkp( [ 0, 0.25  ], [ 60, 63, 65 ] )  ]

// this is to loop after 2 bars
[ mkp( [ 0, 0.25, -2  ], [ 60, 63, 65 ] )  ]


////////////////////////////////////////////////////////////////////////////
// try this example in a drum rack
[ mkp( "0:1", "C1" ),			// Bassdrum
  mkp( "0:2:2.2:-4", "D1"),		// Snare
  mkp( "0:0.5:0.3/4", "A#2") ]	// Hithat


////////////////////////////////////////////////////////////////////////////
// mix chords and single notes, 1 per bars
[ mkp( [ 0, 1 ], [ 60, [65, 70, 74], [72, 75, 77], 64 ] )  ]


////////////////////////////////////////////////////////////////////////////
// choose a note; 4 times
[ mkp( "0:0.75", choose(4, [ 60, 62, 64, 66]) ) ]

